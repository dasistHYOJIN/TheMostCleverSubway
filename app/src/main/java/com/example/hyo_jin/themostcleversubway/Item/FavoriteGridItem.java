public class FavoriteGridItem {

    private String station1;
    private String station2;
    private String cell_seat;
    private String cell_fast;
    private String train;
    private String trans;
    private boolean star;

    public String getStation1() {
        return station1;
    }

    public void setStation1(String station1) {
        this.station1 = station1;
    }

    public String getStation2() {
        return station2;
    }

    public void setStation2(String station2) {
        this.station2 = station2;
    }

    public String getCell_seat() {
        return cell_seat;
    }

    public void setCell_seat(String cell_seat) {
        this.cell_seat = cell_seat;
    }

    public String getCell_fast() {
        return cell_fast;
    }

    public void setCell_fast(String cell_fast) {
        this.cell_fast = cell_fast;
    }

    public String getTrain() {
        return train;
    }

    public void setTrain(String train) {
        this.train = train;
    }

    public String getTrans() {
        return trans;
    }

    public void setTrans(String trans) {
        this.trans = trans;
    }

    public boolean isStar() {
        return star;
    }

    public void setStar(boolean star) {
        this.star = star;
    }
}
